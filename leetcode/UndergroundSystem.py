class UndergroundSystem(object):

    def __init__(self):
        self.user = defaultdict(list)
        self.destination = defaultdict(list)
    def checkIn(self, id, stationName, t):
        """
        :type id: int
        :type stationName: str
        :type t: int
        :rtype: None
        """
        self.user[id] = [stationName, t]
        

    def checkOut(self, id, stationName, t):
        """
        :type id: int
        :type stationName: str
        :type t: int
        :rtype: None
        """
        station, time = self.user[id]
        self.destination[(station, stationName)].append(t-time)
        
        

    def getAverageTime(self, startStation, endStation):
        """
        :type startStation: str
        :type endStation: str
        :rtype: float
        """
        return float(sum(self.destination[(startStation, endStation)]))/len(self.destination[(startStation, endStation)])


